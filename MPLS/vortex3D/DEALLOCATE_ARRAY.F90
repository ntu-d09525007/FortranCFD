SUBROUTINE DEALLOCATE_ARRAY()
USE PROBLEM_DEF
IMPLICIT NONE

 CALL DEALLOCATE_PROBLEM_DEF
 CALL DEALLOCATE_FLUID_PROPERTIES
 CALL DEALLOCATE_LS_DATA
 CALL DEALLOCATE_VOF_DATA
 CALL DEALLOCATE_DUMMY_DATA
 CALL DEALLOCATE_IBM_DATA
 CALL DEALLOCATE_VORTEX_DATA

END SUBROUTINE

SUBROUTINE DEALLOCATE_PROBLEM_DEF
USE PROBLEM_DEF
IMPLICIT NONE

  DEALLOCATE(X,Y,Z)
  
END SUBROUTINE

SUBROUTINE DEALLOCATE_FLUID_PROPERTIES
USE PROBLEM_DEF
USE FLUID_PROPERTIES
IMPLICIT NONE

  DEALLOCATE( U,V,W,UH,VH,WH )
