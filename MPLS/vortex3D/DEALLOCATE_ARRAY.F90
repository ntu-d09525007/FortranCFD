  DEALLOCATE(V_OLD,V_TMP,V1,VS,VS_OLD)
  DEALLOCATE(W_OLD,W_TMP,W1,WS,WS_OLD)
  DEALLOCATE(RHO, RHO_OLD, AMU, AMU_OLD)
  DEALLOCATE(P,P_OLD,P_OLD2,P_TMP,RHS)

