SUBROUTINE RK3_RHO(BTN)
USE DAT
IMPLICIT NONE
INTEGER :: BTN, I
  
  !$OMP PARALLEL DO 
  DO I = -2, N+3
	F(I) = RHO(I)*U_OLD(i)
  END DO 
  !$OMP END PARALLEL DO 
  
  CALL SORCE(S0,RHO,BTN)
  CALL RK3_1(RHO)
  CALL BC(RHO)
  
  !$OMP PARALLEL DO 
  DO I = -2, N+3
	F(I) = RHO(I)*U_OLD(i)
  END DO 
  !$OMP END PARALLEL DO 
  
  CALL SORCE(S1,RHO,BTN)
  CALL RK3_2(RHO)
  CALL BC(RHO)
  
  !$OMP PARALLEL DO 
  DO I = -2, N+3
	F(I) = RHO(I)*U_OLD(i)
  END DO 
  !$OMP END PARALLEL DO 
  
  CALL SORCE(S2,RHO,BTN)
  CALL RK3_3(RHO)
  CALL BC(RHO)
  

END SUBROUTINE




